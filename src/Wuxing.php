    public static function getNumberWuxings(int $year): array
    {
        if (!isset(static::$numberWuxings[$year])) {
            $wuxingNumbers = static::getWuxingNumbers($year);

            $numberWuxings = [];
            foreach ($wuxingNumbers as $wuxing => $numbers) {
                foreach ($numbers as $number) {
                    $numberWuxings[$number] = $wuxing;
                }
